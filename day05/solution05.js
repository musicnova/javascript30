/**
 * @param {number[]} arr
 * @param {Function} fn
 * @return {number[]}
 */
var map = function(arr, fn) {
    
/**
 * Решение задачи 2635. Apply Transform Over Each Element in Array
 *
 * Алгоритм решения:
 * 1. Создаем новый пустой массив, в который будем записывать результаты применения функции fn к каждому элементу исходного массива arr.
 * 2. Проходим циклом по всем элементам массива arr:
 *    - Для каждого элемента вызываем функцию fn, передавая два аргумента: текущий элемент и его индекс.
 *    - Результат вызова fn добавляем в новый массив.
 * 3. После завершения цикла возвращаем новый массив с преобразованными значениями.
 *
 * Важно:
 * - Не используем встроенный метод Array.map, чтобы выполнить задачу вручную.
 * - Функция fn может принимать два параметра: значение элемента и индекс.
 * - Возвращаемый массив должен иметь ту же длину, что и исходный.
 *
 * Сложность алгоритма: O(n), где n - длина массива arr.
 *
 * Пример:
 * arr = [1, 2, 3]
 * fn = function plusone(n) { return n + 1; }
 * Результат: [2, 3, 4]
 */

    // создаем новый массив для результатов
    const result = [];
    // проходим по каждому элементу исходного массива
    for (let i = 0; i < arr.length; i++) {
        // применяем функцию fn к элементу и индексу, сохраняем результат
        result.push(fn(arr[i], i));
    }
    // возвращаем массив с преобразованными элементами
    return result;

/**
 * Ответы на возможные вопросы:
 *
 * Вопрос: Почему нельзя использовать встроенный метод Array.map?
 * Ответ: В условии задачи явно указано не использовать встроенный метод map, чтобы продемонстрировать понимание механизма преобразования массива "вручную".
 *
 * Вопрос: Что делать, если массив пустой?
 * Ответ: Если массив пустой, цикл не выполнится ни разу, и функция вернет пустой массив, что корректно.
 *
 * Вопрос: Можно ли функция fn возвращать не число?
 * Ответ: По условию задачи функция fn возвращает целое число, поэтому мы предполагаем, что возвращаемые значения - числа.
 *
 * Вопрос: Что если функция fn использует индекс?
 * Ответ: Индекс передается вторым аргументом, поэтому функция может использовать его для вычислений.
 *
 * Вопрос: Какова максимальная длина массива?
 * Ответ: По условию длина массива не превышает 1000, что не вызывает проблем с производительностью при простом цикле.
 */

};

