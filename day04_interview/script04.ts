function divideString(s: string, k: number, fill: string): string[] {
/**
 * Алгоритм решения задачи:
 * 
 * 1. Определяем количество групп, на которые нужно разбить строку s. 
 *    Для этого делим длину строки s на k и округляем вверх (Math.ceil).
 *    Это нужно, чтобы учесть последнюю группу, которая может быть неполной.
 * 
 * 2. Создаем пустой массив для хранения групп.
 * 
 * 3. Проходим циклом от 0 до количества групп (не включая).
 *    - Для каждой группы берем подстроку из s, начиная с индекса i * k и длиной k символов.
 *    - Если длина подстроки меньше k (т.е. последняя группа), дополняем её символом fill до длины k.
 * 
 * 4. Добавляем сформированную группу в массив.
 * 
 * 5. По окончании цикла возвращаем массив групп.
 * 
 * Таким образом, мы гарантируем, что:
 * - Все символы строки s распределены по группам без пропусков.
 * - Последняя группа дополнена символами fill, если не хватает символов.
 * - После удаления символов fill из последней группы и объединения всех групп, мы получим исходную строку s.
 */

    const groups: string[] = [];
    const groupCount = Math.ceil(s.length / k);

    for (let i = 0; i < groupCount; i++) {
        // Берем подстроку длиной k, начиная с i*k
        let group = s.slice(i * k, i * k + k);

        // Если длина подстроки меньше k, дополняем символом fill
        if (group.length < k) {
            group = group.padEnd(k, fill);
        }

        groups.push(group);
    }

    return groups;

/**
 * Ответы на возможные вопросы:
 * 
 * Q: Почему мы используем Math.ceil для подсчёта количества групп?
 * A: Потому что если длина строки не кратна k, последняя группа будет неполной, и нам нужна дополнительная группа для оставшихся символов.
 * 
 * Q: Что если строка s уже кратна k?
 * A: Тогда все группы будут ровно длиной k, и символ fill не будет использован.
 * 
 * Q: Можно ли использовать другой символ fill?
 * A: Да, fill может быть любым символом, но согласно условию задачи — это всегда строчная английская буква.
 * 
 * Q: Что гарантирует условие "после удаления fill из последней группы и объединения всех групп получается исходная строка"?
 * A: Это значит, что fill используется только для дополнения последней группы и не влияет на исходные символы строки s.
 * 
 * Q: Какова сложность алгоритма?
 * A: Временная сложность O(n), где n — длина строки s, так как мы проходим по строке один раз с шагом k.
 */
}
